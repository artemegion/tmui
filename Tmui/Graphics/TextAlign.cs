namespace Tmui.Graphics;

/// <summary>
/// Text alignment. Used for both horizontal and vertical alignment.
/// </summary>
public enum TextAlign
{
    /// <summary>
    /// Text should be aligned at the start of the axis.
    /// </summary>
    Start,
    /// <summary>
    /// Text should be aligned at the center of the axis
    /// </summary>
    Center,
    /// <summary>
    /// Text should be aligned at the end of the axis
    /// </summary>
    End
}
